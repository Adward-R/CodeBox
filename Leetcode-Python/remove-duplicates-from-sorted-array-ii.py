class Solution(object):
    def removeDuplicates(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        shamt = 0
        leng = len(nums)
        i = 0
        while i < leng - shamt:
            j = 1
            stdrd = nums[i+shamt]
            try:
                while nums[i+shamt+j] == stdrd:
                    j += 1
            except:
                pass
            if j > 2:
                shamt += j - 2
            for k in range(i, i+j):
                try:
                    nums[k] = nums[k+shamt]
                except:
                    break
            #nums[i:i+j] = nums[i+shamt:i+j+shamt]
            i += min(j, 2)
        return leng-shamt

